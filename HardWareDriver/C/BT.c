#include "BT.h"
#include "UART1.h"

/********************************************
              Led初始化函数
功能：
1.配置Led接口IO输出方向
2.关闭所有Led(开机默认方式)

描述：
Led接口：

BT_EN-->PB2
对应IO=1，灯亮
********************************************/
void BT_PowerInit(void)
{
    RCC->APB2ENR|=1<<3;    //使能PORTB时钟	

    GPIOB->CRL&=0XFFFFF0FF;  //PB2推挽输出
    GPIOB->CRL|=0X00000300;
    GPIOB->ODR|=1<<2;        //PB2上拉
    BT_off();                  //默认关闭

}



